# 1. Even or Odd solution

def even_or_odd(number):
    if number % 2 == 0:
        return "Even"
    else:
        return "Odd"

# 2. Convert a Number to a String solution
def number_to_string(num):
    return str(num)

# 3. Remove String Spaces solution
def no_space(x):
    return x.replace(" ", "")